package week9.day1;

public class LearnSwitchCase {
	public static void main(String[] args) {
		String browser="edge";
		
		switch(browser) {
		
		case "Chrome":
		System.out.println("Chrome brower opened");
		break;
		case "Edge":
		System.out.println("Edge browser opened");
		break;
		 default:
		System.out.println("Any of the browser");
		}
		
	}
}
