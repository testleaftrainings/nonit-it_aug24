package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {

		int number=5;
		//for -> ctrl+space
		for (int i = 0; i <=number; i++) {
			System.out.println(i);
		}
		
		
		//5,4,3,2,1,0
		
		for (int i =number; i>=0 ; i--) {
			System.out.println(i);
		}
		
		}

}
